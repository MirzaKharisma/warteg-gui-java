    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        if (CariByKode(Kode.getText()) != Menu.menu.size()) {
            try {
                Menu.menu.set(CariByKode(Kode.getText()), new Menu(Kode.getText(), Makanan.getText(), Integer.parseInt(Jumlah.getText()), Integer.parseInt(Harga.getText()), Integer.parseInt(Total.getText())));
                Data.writeFile();
                tampildata();
                JOptionPane.showMessageDialog(this, "Sukses Ubah Data !", "Sukses", JOptionPane.DEFAULT_OPTION);
                Reset();
            } catch (Exception e) {
                System.out.println("Error : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ada", "Error", JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_UpdateActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        int hapus = JOptionPane.showConfirmDialog(this, "Anda yakin Menghapus Data ?", "Hapus", JOptionPane.YES_NO_OPTION);
        if (hapus == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Data batal dihapus !");
        } else if (hapus == JOptionPane.CLOSED_OPTION) {

        } else if (CariByKode(Kode.getText()) != Menu.menu.size()) {
            try {
                Menu.menu.remove(CariByKode(Kode.getText()));
                if (Menu.menu.isEmpty()) {
                    DefaultTableModel a = (DefaultTableModel) tabel.getModel();
                    a.removeRow(0);
                }
                Data.writeFile();
                tampildata();
                JOptionPane.showMessageDialog(this, "Sukses Hapus Data !", "Sukses", JOptionPane.DEFAULT_OPTION);
                Reset();
            } catch (Exception e) {
                System.out.println("Error : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ada", "Error", JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_HapusActionPerformed

