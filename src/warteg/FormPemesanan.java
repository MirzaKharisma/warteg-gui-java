    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i = CariByKode(Cari.getText());
        if (i >= Menu.menu.size()) {
            JOptionPane.showMessageDialog(this, "Data kosong!");
            tampildata();
        } else {
            DefaultTableModel a = (DefaultTableModel) tabel.getModel();
            a.getDataVector().removeAllElements();
            Object[] baris = new Object[5];

            baris[0] = Menu.menu.get(i).getKode_makanan();
            baris[1] = Menu.menu.get(i).getNama_makanan();
            baris[2] = Menu.menu.get(i).getJumlah();
            baris[3] = Menu.menu.get(i).getHarga();
            baris[4] = Menu.menu.get(i).getTotal();
            a.addRow(baris);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void Reset() {
        Kode.setText("");
        Makanan.setText("");
        Harga.setText("0");
        Jumlah.setText("0");
        Total.setText("0");
        Kode.setEnabled(true);
        Update.setVisible(false);
        Hapus.setVisible(false);
        Reset.setVisible(false);
        Tambah.setVisible(true);
    }

    public int CariByKode(String Kode) {
        int i = 0;
        for (; i < Menu.menu.size(); i++) {
            if (Menu.menu.get(i).getKode_makanan().equals(Kode)) {
                break;
            }
        }
        return i;
    }

